@SuppressWarnings("serial")
/**
 * A DecisionTreeException is thrown by objects in this package when an
 * exceptional case is encountered.
 * 
 * @author jmac
 */
public class DecisionTreeException extends Exception {

	/**
	 * Create a new ClassifierException, and associate the given message with
	 * it.
	 * 
	 * @param message
	 */
	public DecisionTreeException(String message) {
		super(message);
	}

}
